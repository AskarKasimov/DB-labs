SELECT
    Н_ЛЮДИ.ИД,
    Н_ОБУЧЕНИЯ.ЧЛВК_ИД,
    Н_УЧЕНИКИ.ГРУППА
FROM
    Н_ЛЮДИ
    RIGHT JOIN Н_ОБУЧЕНИЯ ON Н_ЛЮДИ.ИД = Н_ОБУЧЕНИЯ.ЧЛВК_ИД
    RIGHT JOIN Н_УЧЕНИКИ ON Н_ОБУЧЕНИЯ.ЧЛВК_ИД = Н_УЧЕНИКИ.ИД
WHERE
    Н_ЛЮДИ.ИМЯ = 'Александр'
    AND Н_ОБУЧЕНИЯ.ЧЛВК_ИД > 163276
    AND Н_УЧЕНИКИ.НАЧАЛО < '2009-02-09';